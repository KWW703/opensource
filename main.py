# 무작위로 메뉴를 섞기 위해 random 모듈을 가져옵니다.
import random 

# 점심 메뉴 목록: 32개의 다양한 메뉴를 포함하고 있습니다.
메뉴 = [
    "김치찌개", "된장찌개", "비빔밥", "불고기",
    "칼국수", "냉면", "제육볶음", "닭갈비",
    "돈까스", "초밥", "라멘", "파스타",
    "짜장면", "짬뽕", "햄버거", "피자",
    "떡볶이", "순두부찌개", "쭈꾸미볶음", "갈비탕",
    "삼겹살", "감자탕", "치킨", "족발",
    "부대찌개", "소고기국밥", "쌀국수", "우동",
    "샐러드", "토스트", "스테이크", "리조또"
]

# 각 라운드 번호에 대응하는 대진 이름을 딕셔너리로 정의합니다.
라운드to대진 = {
    1: "32강",  # 첫 번째 라운드는 32강
    2: "16강",  # 두 번째 라운드는 16강
    3: "8강",   # 세 번째 라운드는 8강
    4: "숨막히는 준결승전",  # 네 번째 라운드는 준결승
    5: "대망의 결승전"      # 마지막 라운드는 결승전
}

# 사용자로부터 토너먼트 단계를 선택받습니다.
while True:
    # 선택지 출력
    print("토너먼트를 선택하세요:") 
    # 선택 가능한 토너먼트 크기
    print("1: 32강\n2: 16강\n3: 8강")  
    # 사용자 입력 받기
    선택 = input("번호를 입력하세요 (1, 2, 3): ") 
     # 올바른 입력인지 확인
    if 선택 in ["1", "2", "3"]: 
	# 입력값을 정수로 변환
        선택된단계 = int(선택)  
        # 유효한 입력이면 루프 종료
        break  
    else: 
        # 잘못된 입력 메시지 출력
        print("⚠️ 잘못된 입력입니다. 1, 2, 3 중 하나를 선택하세요.")  

# 선택된 토너먼트 단계에 따라 초기 메뉴 수와 시작 라운드를 설정합니다.
 # 32강 선택 시
if 선택된단계 == 1: 
 # 메뉴를 32개로 설정
    초기강 = 32 
# 첫 라운드는 32강
    시작라운드 = 1  
# 16강 선택 시
elif 선택된단계 == 2:   
 # 메뉴를 16개로 설정
    초기강 = 16 
# 첫 라운드는 16강
    시작라운드 = 2  
# 8강 선택 시
else:  
# 메뉴를 8개로 설정
    초기강 = 8  
 # 첫 라운드는 8강
    시작라운드 = 3 

# 선택된 토너먼트 단계에 맞게 메뉴 리스트를 잘라냅니다.
메뉴 = 메뉴[:초기강]  

# 토너먼트 시작 단계 출력
print(f"\n=== 토너먼트 시작: {라운드to대진[시작라운드]} ===")

# 라운드를 시작합니다.
라운드 = 시작라운드
# 최종 메뉴가 하나가 될 때까지 반복
while len(메뉴) > 1:  
# 현재 라운드 이름 출력
    print(f"\n=== {라운드to대진[라운드]} ===")  
 # 각 라운드의 승자 메뉴를 저장할 리스트
    이긴메뉴 = [] 
# 메뉴를 무작위로 섞어 대진표를 만듭니다.
    random.shuffle(메뉴)  
    
    # 두 메뉴씩 짝을 지어 대결을 진행합니다.
    # 인덱스를 2씩 증가시켜 대진을 만듭니다.
    for i in range(0, len(메뉴)-1, 2): 
    # 두 메뉴를 한 쌍으로 묶음
        대결 = [메뉴[i], 메뉴[i + 1]]  
        while True:
      # 대결 출력
            print(f"어떤 메뉴를 고르시겠습니까? {대결[0]} vs {대결[1]}") 
       # 사용자 입력
            승자선택 = int(input("선호하는 메뉴를 선택하세요 (1 또는 2): "))  
        # 올바른 입력인지 확인
            if 승자선택 == 1 or 승자선택 == 2: 
                승자 = 대결[승자선택-1] 
             # 선택한 메뉴를 승자로 설정
                이긴메뉴.append(승자)  
            # 승자를 다음 라운드 리스트에 추가
            # 유효한 입력이면 루프 종료
                break  
            else:
                print("=" * 20)
              # 잘못된 입력 메시지 출력
                print("⚠️ 잘못된 입력입니다. 1 또는 2를 입력하세요.") 
    
    # 현재 라운드에서 이긴 메뉴들을 출력합니다.
    print("다음 라운드에 진출한 메뉴:", 이긴메뉴)
     # 다음 라운드의 메뉴로 갱신
    메뉴 = 이긴메뉴 
    라운드 += 1  # 다음 라운드로 넘어갑니다.

# 최종적으로 선택된 메뉴를 출력합니다.
print(f"\n오늘의 점심 메뉴는 {메뉴[0]} 입니다! 🍴")



