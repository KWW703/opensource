import random

# 점심 메뉴 목록
메뉴 = [
    "김치찌개", "된장찌개", "비빔밥", "불고기",
    "칼국수", "냉면", "제육볶음", "닭갈비",
    "돈까스", "초밥", "라멘", "파스타",
    "짜장면", "짬뽕", "햄버거", "피자",
    "떡볶이", "순두부찌개", "쭈꾸미볶음", "갈비탕",
    "삼겹살", "감자탕", "치킨", "족발",
    "부대찌개", "소고기국밥", "쌀국수", "우동",
    "샐러드", "토스트", "스테이크", "리조또"
]

라운드 = 1
라운드to대진 = {
    1: "32강",
    2: "16강",
    3: "8강",
    4: "숨막히는 준결승전",
    5: "대망의 결승전",
}

while len(메뉴) > 1:
    print(f"\n=== {라운드to대진[라운드]} ===")
    이긴메뉴 = []
    random.shuffle(메뉴)
    
    for i in range(0, len(메뉴)-1, 2):
        대결 = [메뉴[i], 메뉴[i + 1]]
        while True:
            print(f"어떤 메뉴를 고르시겠습니까? {대결[0]} vs {대결[1]}")
            승자선택 = int(input("선호하는 메뉴를 선택하세요 (1 또는 2): "))
            if 승자선택 == 1 or 승자선택 == 2:
                승자 = 대결[승자선택-1]
                이긴메뉴.append(승자)
                break
            else:
                print("=" * 20)
                print("⚠️ 잘못된 입력입니다. 1 또는 2를 입력하세요.")
    
    print("다음 라운드에 진출한 메뉴:", 이긴메뉴)
    메뉴 = 이긴메뉴
    라운드 += 1

print(f"\n오늘의 점심 메뉴는 {메뉴[0]} 입니다! 🍴")


